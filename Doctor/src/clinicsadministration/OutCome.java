    private void weekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_weekActionPerformed
        // TODO add your handling code here:
        if (week.isSelected()) {
            String statement = "SELECT * FROM expenserecord WHERE ( date = ";
            String days[] = Tools.get7Days();
            for (int i = 0; i < days.length; i++) {
                if (i < days.length - 1) {
                    statement += "'" + days[i] + "' OR date = ";
                } else {
                    statement += "'" + days[i] + "' ) ;";
                }
            }
            updateTable(statement);
            txttotal.setText(String.valueOf(totalTable()));
        }
    }//GEN-LAST:event_weekActionPerformed

