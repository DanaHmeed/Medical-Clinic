    public void filterTable(String serch) {
        serch = serch.replace('\\', ' ');
        serch = serch.replace('/', ' ');
        serch = serch.replace('*', ' ');
        serch = serch.replace('-', ' ');
        serch = serch.replace('+', ' ');
        serch = serch.replace('=', ' ');
        serch = serch.replace('&', ' ');
        serch = serch.replace('^', ' ');
        serch = serch.replace('%', ' ');
        serch = serch.replace('$', ' ');
        serch = serch.replace('#', ' ');
        serch = serch.replace('@', ' ');
        serch = serch.replace('!', ' ');
        serch = serch.replace('<', ' ');
        serch = serch.replace('>', ' ');
        try {
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
            table.setRowSorter(tr);
            tr.setRowFilter(RowFilter.regexFilter(serch.trim()));

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "خطأ لا يمكن ادخل الرموز");
        }

    }

    public int totalTable() {
        int total = 0;
        for (int i = 0; i < table.getRowCount(); i++) {
            total += Integer.parseInt(String.valueOf(table.getValueAt(i, 1)));
        }
        return total;
    }

