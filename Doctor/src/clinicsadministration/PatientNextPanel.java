    private void tableUpdate() {
        ImageIcon icon = new ImageIcon(getClass().getResource("/images/delete.png"));
        Object[][] arr = new Object[5][5];
        String statement = "SELECT * FROM next ;";
        ResultSet rs = Tools.select_query(statement);
        try {
            int rowcount = 0, j = 0;
            while (rs.next()) {
                rowcount++;
            }
            arr = new Object[rowcount][7];
            rs = Tools.select_query(statement);
            while (rs.next()) {
                for (int i = 0; i < 6; i++) {
                    arr[j][i] = rs.getString(i + 1);
                }
                arr[j][6] = icon;
                j++;
            }
            Tools.closeConnection();
        } catch (Exception ex) {
        }
        Tools.closeConnection();
        DefaultTableModel model = new DefaultTableModel(arr,
                new String[]{
                    "رقم الدور", "الاسم", "العياده", "الطبيب المعالج", "التاريخ", "الوقت", "حذف"
                }) {
            @Override
            public Class<?> getColumnClass(int column) {
                switch (column) {
                    case 0:
                    case 1:
                    case 2:
                    case 3:
                    case 4:
                    case 5:
                        return Object.class;
                    case 6:
                        return ImageIcon.class;
                    default:
                        return Object.class;
                }
            }
        };
        table.setModel(model);
        DefaultTableCellRenderer renderer = new DefaultTableCellRenderer();
        renderer.setHorizontalAlignment(JLabel.CENTER);
        for (int i = 0; i < table.getColumnCount() - 1; i++) {
            table.getColumnModel().getColumn(i).setCellRenderer(renderer);
        }
    }

    public void filterTable(String serch) {
        serch = serch.replace('\\', ' ');
        serch = serch.replace('/', ' ');
        serch = serch.replace('*', ' ');
        serch = serch.replace('-', ' ');
        serch = serch.replace('+', ' ');
        serch = serch.replace('=', ' ');
        serch = serch.replace('&', ' ');
        serch = serch.replace('^', ' ');
        serch = serch.replace('%', ' ');
        serch = serch.replace('$', ' ');
        serch = serch.replace('#', ' ');
        serch = serch.replace('@', ' ');
        serch = serch.replace('!', ' ');
        serch = serch.replace('<', ' ');
        serch = serch.replace('>', ' ');
        try {
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
            table.setRowSorter(tr);
            tr.setRowFilter(RowFilter.regexFilter(serch.trim()));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "خطأ لا يمكن ادخل الرموز");
        }
    }

