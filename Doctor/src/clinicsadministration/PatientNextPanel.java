    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        if (row > -1 && table.getSelectedColumn() == 6) {
            int ok = JOptionPane.showConfirmDialog(null, "هل تريد الحذف بالتأكيد ؟", "اختيار", JOptionPane.YES_NO_OPTION);
            if (ok == 0) {
                String statement = "DELETE FROM next WHERE id = " + table.getValueAt(row, 0) + " ;";
                boolean check = Tools.ExecuteStatement(statement);
                if (check) {
                    JOptionPane.showMessageDialog(null, "تم الحذف بنجاح");
                }
                Tools.closeConnection();
                tableUpdate();
            }
        }
    }//GEN-LAST:event_tableMouseClicked

