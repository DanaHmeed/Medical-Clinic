    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        this.row = row;
        txtcode.setText(String.valueOf(table.getValueAt(row, 0)));
        txtname.setText(String.valueOf(table.getValueAt(row, 1)));
        txtaddress.setText(String.valueOf(table.getValueAt(row, 2)));
        txtphone.setText(String.valueOf(table.getValueAt(row, 3)));
        txtemail.setText(String.valueOf(table.getValueAt(row, 4)));
        txtjob.setText(String.valueOf(table.getValueAt(row, 5)));
        txtsalary.setText(String.valueOf(table.getValueAt(row, 6)));
        combosex.setSelectedItem(String.valueOf(table.getValueAt(row, 7)));
        txtid.setText(String.valueOf(table.getValueAt(row, 8)));
        txtdate.setText(String.valueOf(table.getValueAt(row, 9)));
        ImageIcon icon = Tools.getImageIcon("SELECT imagefile FROM employee WHERE id = " + table.getValueAt(row, 0) + "  ;");
        if (icon != null) {
            icone.setIcon(icon);
        } else {
            icone.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/no-image.png")));
        }
        Tools.closeConnection();
        if(table.getSelectedColumn() == 10 && !String.valueOf(table.getValueAt(row, 10)).isEmpty() && !String.valueOf(table.getValueAt(row, 10)).equals("null")){
            String statement = "SELECT imagefile FROM employee WHERE id = "+table.getValueAt(row, 0)+" ;";
            new ImageZoom(null, true, statement, String.valueOf(table.getValueAt(row, 10))).setVisible(true);
        }
    }//GEN-LAST:event_tableMouseClicked

