    public void filterTable(String serch) {
        try {
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
            table.setRowSorter(tr);
            tr.setRowFilter(RowFilter.regexFilter(serch.trim()));

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "خطأ لا يمكن ادخل الرموز");
        }

    }

    public void btnSearch() {
        if (!txtsearch.getText().isEmpty()) {
            if (combochoose.getSelectedIndex() == 0) {
                int id = 0;
                try {
                    id = Integer.parseInt(txtsearch.getText().trim());
                    String statement = "SELECT * FROM employee WHERE id = " + id + " ;";
                    DefaultTableModel model = (DefaultTableModel) table.getModel();
                    Tools.tableUpdate(model, statement, 11);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "يجب ادخال قيمه عدديه ");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "يجب ادخال قيمه في خانه البحث");
        }
    }

    public int sumtotal() {
        int total = 0;
        for (int i = 0; i < table.getRowCount(); i++) {
            total = Integer.parseInt(String.valueOf(table.getValueAt(i, 6)));
        }
        return total;
    }

