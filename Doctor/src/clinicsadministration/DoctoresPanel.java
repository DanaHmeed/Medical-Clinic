    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        this.row = row;
        txtcode.setText(String.valueOf(table.getValueAt(row, 0)));
        txtname.setText(String.valueOf(table.getValueAt(row, 1)));
        txtaddress.setText(String.valueOf(table.getValueAt(row, 2)));
        txtphone.setText(String.valueOf(table.getValueAt(row, 3)));
        txtemail.setText(String.valueOf(table.getValueAt(row, 4)));
        combospecial.setSelectedItem(String.valueOf(table.getValueAt(row, 5)));
        combosex.setSelectedItem(String.valueOf(table.getValueAt(row, 6)));
        txtsalary.setText(String.valueOf(table.getValueAt(row, 7)));
        if (String.valueOf(table.getValueAt(row, 8)).equals("0")) {
            radiomonth.setSelected(true);
        } else {
            radiopercent.setSelected(true);
        }
        ImageIcon icon = Tools.getImageIcon("SELECT imagefile FROM doctors WHERE id = " + txtcode.getText() + " ;");
        if (icon != null) {
            icone.setIcon(icon);
        } else {
            icone.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/no-image.png")));
        }
        Tools.closeConnection();
        if (table.getSelectedColumn() == 9 && !String.valueOf(table.getValueAt(row, 9)).isEmpty() && 
                !String.valueOf(table.getValueAt(row, 9)).equals("null")) {
            String statement = "SELECT imagefile FROM doctors WHERE id = " + table.getValueAt(row, 0) + " ;";
            new ImageZoom(null, true, statement, String.valueOf(table.getValueAt(row, 9))).setVisible(true);
        }
    }//GEN-LAST:event_tableMouseClicked

