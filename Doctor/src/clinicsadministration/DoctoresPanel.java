    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
        ResultSet rs = Tools.select_query("SELECT * FROM doctors; ");
        int count = 1;
        try {
            while (rs.next()) {
                count++;
            }
        } catch (Exception ex) {
        }
        if (count <= 5) {
            if (!txtname.getText().isEmpty() && combospecial.getSelectedIndex() > -1) {
                boolean cheack = false;
                if (addphoto.isEmpty()) {
                    String statement = "INSERT INTO doctors (name, address, phone, email, special, sex, salary, period) "
                            + "VALUES "
                            + "('" + txtname.getText() + "', '" + txtaddress.getText() + "', '" + txtphone.getText() + "',"
                            + " '" + txtemail.getText() + "', "
                            + "'" + idclincname.get(combospecial.getSelectedIndex()) + "', " + combosex.getSelectedIndex() + ", "
                            + " " + txtsalary.getText() + ", " + getPeriodValue() + " ) ;";
                    cheack = Tools.ExecuteStatement(statement);
                } else {
                    File file = new File(addphoto);
                    String statement = "INSERT INTO doctors (name, address, phone, email, special, sex, salary, period, "
                            + "imagename, imagefile) "
                            + "VALUES "
                            + "('" + txtname.getText() + "', '" + txtaddress.getText() + "', '" + txtphone.getText() + "',"
                            + " '" + txtemail.getText() + "', "
                            + "" + idclincname.get(combospecial.getSelectedIndex()) + ", " + combosex.getSelectedIndex() + ", "
                            + " " + txtsalary.getText() + ", " + getPeriodValue() + ", '" + file.getName() + "', ? ) ;";
                    cheack = Tools.ExecuteStatementWithPicture(statement, addphoto);
                }
                if (cheack) {
                    JOptionPane.showMessageDialog(null, "تمت الاضافه بنجاح");
                    updateTable();
                    resetValue();
                }
                Tools.closeConnection();
            } else {
                JOptionPane.showMessageDialog(null, "يجب اضافه اسم الدكتور");
            }
        }else
            JOptionPane.showMessageDialog(null, "يجب شراء النسخه الكامله لتهيئه الحد الاستيعابي للبرنامج");
    }//GEN-LAST:event_btnaddActionPerformed

